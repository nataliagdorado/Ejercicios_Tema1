public class Ejercicio2 {
    public static void main(String[] args) {
        char maxChar = Character.MAX_VALUE;
        short maxShort = Short.MAX_VALUE;
        int maxInt = Integer.MAX_VALUE;

        System.out.println("El mayor número entero representable por un char es: " + (int) maxChar);
        System.out.println("El mayor número entero representable por un short es: " + maxShort);
        System.out.println("El mayor número entero representable por un int es: " + maxInt);
    }
}
